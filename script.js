const root = document.documentElement;
const header = document.getElementById('siteHeader');
const languageToggle = document.getElementById('languageToggle');
const themeToggle = document.getElementById('themeToggle');
const progress = document.getElementById('scrollProgress');
const backToTop = document.getElementById('backToTop');
const currentYear = document.getElementById('currentYear');

const safeStorage = {
  get(key) {
    try {
      return localStorage.getItem(key);
    } catch {
      return null;
    }
  },

  set(key, value) {
    try {
      localStorage.setItem(key, value);
    } catch {
      // 浏览器禁止本地存储时，网页仍然可以正常使用
    }
  }
};

function applyLanguage(language) {
  const dictionary = translations[language] || translations.en;

  document.querySelectorAll('[data-i18n]').forEach((element) => {
    const key = element.dataset.i18n;

    if (dictionary[key]) {
      element.textContent = dictionary[key];
    }
  });

  root.lang = language === 'zh' ? 'zh-CN' : 'en';

  languageToggle.textContent = language === 'zh' ? 'EN' : '中文';
  languageToggle.dataset.language = language;

  languageToggle.setAttribute(
    'aria-label',
    language === 'zh' ? 'Switch to English' : '切换到中文'
  );

  document.title =
    language === 'zh'
      ? '林灿然（Canran Bruce Lin）｜福州 · 滑铁卢大学'
      : 'Canran (Bruce) Lin | Fuzhou · University of Waterloo';

  safeStorage.set('preferred-language', language);
}

const initialLanguage =
  safeStorage.get('preferred-language') === 'zh' ? 'zh' : 'en';

applyLanguage(initialLanguage);

languageToggle.addEventListener('click', () => {
  const nextLanguage =
    languageToggle.dataset.language === 'zh' ? 'en' : 'zh';

  applyLanguage(nextLanguage);
});

function applyTheme(theme) {
  root.dataset.theme = theme;
  themeToggle.textContent = theme === 'dark' ? '☀' : '◐';

  themeToggle.setAttribute(
    'aria-label',
    theme === 'dark'
      ? 'Switch to light theme'
      : 'Switch to dark theme'
  );

  safeStorage.set('preferred-theme', theme);
}

const savedTheme = safeStorage.get('preferred-theme');

const systemDark =
  window.matchMedia &&
  window.matchMedia('(prefers-color-scheme: dark)').matches;

applyTheme(
  savedTheme === 'dark' || savedTheme === 'light'
    ? savedTheme
    : systemDark
      ? 'dark'
      : 'light'
);

themeToggle.addEventListener('click', () => {
  applyTheme(root.dataset.theme === 'dark' ? 'light' : 'dark');
});

function updateScrollUI() {
  const top = window.scrollY || document.documentElement.scrollTop;

  const total =
    document.documentElement.scrollHeight - window.innerHeight;

  const percentage =
    total > 0 ? Math.min(100, (top / total) * 100) : 0;

  progress.style.width = `${percentage}%`;

  header.classList.toggle('scrolled', top > 10);
  backToTop.classList.toggle('visible', top > 650);
}

window.addEventListener('scroll', updateScrollUI, {
  passive: true
});

window.addEventListener('resize', updateScrollUI);

updateScrollUI();

backToTop.addEventListener('click', () => {
  window.scrollTo({
    top: 0,
    behavior: 'smooth'
  });
});

